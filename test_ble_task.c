// Task
static void test_task(void *pvParameters)
{
    for (;;)
    {
        BLEAppUtil_invokeFunction(test_task_helper, NULL);
        vTaskDelay(pdMS_TO_TICKS(1000)); 
    }
}

// Task Creation Function
void create_test_task(void)
{   
    xTaskCreate(test_task,       
                "Test task",             
                TASK_STACK_SIZE,   
                NULL,                     
                TASK_PRIORITY,     
                &test_task_handle);  
}